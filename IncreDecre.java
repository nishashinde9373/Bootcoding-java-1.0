class IncreDecre{

public static void main(String args[]){

int a=10;
System.out.println("Given Number:"+a); 

++a;
System.out.println("pre increment:"+a); 

a++;
System.out.println("pre increment:"+a); 

a--;
System.out.println("pre increment:"+a); 

--a;
System.out.println("pre increment:"+a); 

}
}