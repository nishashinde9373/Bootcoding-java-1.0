class ClassifyAge
{
 public static void main(String args[])
 {
  int age=0;
  if(age<1)
  {
   System.out.println("Age is infant");
  }
  else if(age<10)
  {
   System.out.println("Age is child");
  }
  else if(age<15)
  {
   System.out.println("Age is Teenager");
  }
  else
  {
   System.out.println("Age is Adult");
  }
 }
}